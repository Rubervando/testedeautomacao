const elementos = require('../support/Elements/GlobalElements').ELEMENTS

Cypress.Commands.add('logado', (emailUsuario, senhaUsuario) => {
    cy.get(elementos.emailLogin).type(emailUsuario)
    cy.get(elementos.senhaLogin).type(senhaUsuario)
    cy.get(elementos.botaoLogin).click()
})
